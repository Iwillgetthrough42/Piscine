#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int k;

	while (*str != '\0')
	{
		k = *str;
		ft_putchar(k);
		str++;
	}
}
