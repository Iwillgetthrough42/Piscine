int		ft_is_prime(int nb)
{
	int i;
	int t;

	t = 1;
	i = 2;
	while (i < nb)
	{
		if (nb % i == 0)
		{
			t = 0;
			break ;
		}
		i++;
	}
	if (nb <= 1)
	{
		t = 0;
	}
	return (t);
}
